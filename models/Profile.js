const mongoose = require("mongoose");

const profileSchema = mongoose.Schema({
    user:{
        type:mongoose.Schema.Types.ObjectId,
        ref:"user"
    },
    company:{
        type:String
    },
    website:{
        type:String
    },
    location:{
        type:String
    },
    status :{
        type:String,
        required:true
    },
    skills:{
        type:[String],
        required:true
    },
    bio:{
        type:String
    },
    gitHubUsername:{
        type:String
    },
    exprerience:[{
        title:{
            type:String,
            required:true
        },
        comapany:{
            type:String,
            required:true
        },
        location:{
            type:String
        },
        from:{
            type:Date,
            required:true
        },
        to:{
            type:Date
        },
        current:{
            type:Boolean,
            default:false
        },
        description:{
            type:String
        }
    }],
    education:[
        {
            school:{
                type:String,
                required:true
            },
            degree:{
                type:String,
                required:true
            },
            fieldOfStudy:{
                type:String,
                required:true
            },
            from:{
                type:Date,
                required:true
            },
            to:{
                type:Date,
                required:true
            },
            current:{
                type:Boolean,
                default:false
            },
            description:{
                type:String
            }
        }
    ],
    social:{
        linkedin:{
            type:String
        },
        twitter:{
            type:String
        },
        instagram:{
            type:String
        },
        facebook:{
            type:String
        }
    },
    date:{
        type:Date,
        default:Date.now
    }
});

module.exports = Profile = mongoose.model("profile",profileSchema)